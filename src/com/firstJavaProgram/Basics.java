package com.firstJavaProgram;

public class Basics {
    public static void main(String[] args) {
        if(true){
            System.out.println("Hello world");
        }

        int count = 1;

        //while
        while(count <= 5){
            System.out.print(count);
            count++;
        }

        //for
        for(int i = 1; i <= 5; i++){
            System.out.println(i);
        }

    }
}
